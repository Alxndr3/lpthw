# The \t (backslash t) inserts a tab space in the sentence.
tabby_cat = "\tI'm Tabbed in."

# \non splits the string into sepparated lines.
persian_cat = "I'm split \non a line."

# The \\ inserts a backslash in the string.
backslash_cat = "I'm \\ a \\ cat."

fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)

